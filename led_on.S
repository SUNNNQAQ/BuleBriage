//引脚GPF4

/*  操作过程  */
/*  配置GPF4为输出引脚--把0x100写到地址0x56000050上 */
/*  配置GPF4输出高电平--把0x10 写到地址0x56000054上 */

.text
.global _start

_start:
/*  配置GPF4为输出引脚--把0x100写到地址0x56000050上 */
    ldr r1, =0x56000050
	ldr r0, =0x100 /*mov r0, #0x100*/
	str r0, [r1]

/*  配置GPF4输出高电平--把0x10 写到地址0x56000054上 */
    ldr r1, =0x56000054
	ldr r0, =0x000 /*mov r0, #0x100*/
	str r0, [r1]
    /*  死循环  */
halt:
     b halt

 











